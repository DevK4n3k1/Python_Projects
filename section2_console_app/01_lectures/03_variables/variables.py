##############################################
# Updating Variables
##############################################
x = 10
print(x)  # Output: 10

x = 20  # Updating the variable
print(x)  # Output: 20



##############################################
# Multiple Variables Assignment
##############################################
a, b, c = 5, 10, 15
print(a, b, c)  # Output: 5 10 15s

x = y = z = 100
print(x, y, z)  # Output: 100 100 100


